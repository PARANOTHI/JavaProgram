package ConditionalStatements;

public class If_Else {

	public static void main(String[] args) {
		int mark=89;
		if(mark>49) {
			System.out.println("Pass");
		}
		else {
			System.out.println("Fail");
		}
		
		
		//odd or even
		//int num=10;
		if(mark%2==0) {
			System.out.println("Even");
		}
		else {
			System.out.println("odd");
		}

	}

}
