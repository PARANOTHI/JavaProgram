package Operators;

public class Relational {

	public static void main(String[] args) {
		System.out.println(5<10);
		System.out.println(5>10);
		System.out.println(5<=10);
		System.out.println(5>=10);
		System.out.println(5==10);
		System.out.println(5!=10);
		// it gives output as true or false
	}

}
